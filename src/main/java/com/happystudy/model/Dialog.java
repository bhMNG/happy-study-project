package com.happystudy.model;

public class Dialog {
	private Integer id;
	private String dlAction;
	private String dlTime;
	private String dlTriggerObj;
	private String dlInfo;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getDlAction() {
		return dlAction;
	}
	public void setDlAction(String dlAction) {
		this.dlAction = dlAction;
	}
	public String getDlTime() {
		return dlTime;
	}
	public void setDlTime(String dlTime) {
		this.dlTime = dlTime;
	}
	public String getDlTriggerObj() {
		return dlTriggerObj;
	}
	public void setDlTriggerObj(String dlTriggerObj) {
		this.dlTriggerObj = dlTriggerObj;
	}
	public String getDlInfo() {
		return dlInfo;
	}
	public void setDlInfo(String dlInfo) {
		this.dlInfo = dlInfo;
	}
	
	
	
}
